add = lambda num1 , num2 : num1 + num2
subtract = lambda num1 , num2 : num1 - num2
multiply = lambda num1 , num2 : num1 * num2
divide = lambda num1 , num2 : num1 / num2

def main():
    print(add(1 , 3))
    print(subtract(45 , 22))
    print(multiply(2 , 9))
    print(divide(8 , 2))


if __name__ == '__main__':
    main()