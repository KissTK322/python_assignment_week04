def add(num1 , num2) :
    return num1 + num2

def subtract(num1 , num2) :
    return num1 - num2

def multiply(num1 , num2) :
    return num1 * num2

def divide(num1 , num2) :
    return num1 / num2


def main():
    print(add(1 , 3))
    print(subtract(45 , 22))
    print(multiply(2 , 9))
    print(divide(8 , 2))


if __name__ == '__main__':
    main()